const languageButtons = document.querySelectorAll("[data-language]");
const translatableElements = document.querySelectorAll("[data-i18n]");
const translatableAltElements = document.querySelectorAll("[data-i18n-alt]");
const translatableAriaElements = document.querySelectorAll("[data-i18n-aria]");
const descriptionMeta = document.querySelector('meta[name="description"]');

function setLanguage(language) {
  const selectedLanguage = translations[language] ? language : "ru";
  const dictionary = translations[selectedLanguage];

  document.documentElement.lang = selectedLanguage;
  document.title = dictionary.metaTitle;
  descriptionMeta?.setAttribute("content", dictionary.metaDescription);

  translatableElements.forEach((element) => {
    const key = element.dataset.i18n;
    if (dictionary[key]) element.textContent = dictionary[key];
  });

  translatableAltElements.forEach((element) => {
    const key = element.dataset.i18nAlt;
    if (dictionary[key]) element.setAttribute("alt", dictionary[key]);
  });

  translatableAriaElements.forEach((element) => {
    const key = element.dataset.i18nAria;
    if (dictionary[key]) element.setAttribute("aria-label", dictionary[key]);
  });

  languageButtons.forEach((button) => {
    const isActive = button.dataset.language === selectedLanguage;
    button.classList.toggle("is-active", isActive);
    button.setAttribute("aria-pressed", String(isActive));
  });

  localStorage.setItem("resume-language", selectedLanguage);
}

languageButtons.forEach((button) => {
  button.addEventListener("click", () => setLanguage(button.dataset.language));
});

const savedLanguage = localStorage.getItem("resume-language");
const browserLanguage = navigator.language.toLowerCase().startsWith("ru") ? "ru" : "en";
setLanguage(savedLanguage || browserLanguage);

document.getElementById("current-year").textContent = new Date().getFullYear();

const galleryShells = document.querySelectorAll(".project-gallery-shell");

galleryShells.forEach((shell) => {
  const gallery = shell.querySelector(".project-gallery");
  const previousButton = shell.querySelector(".gallery-nav--prev");
  const nextButton = shell.querySelector(".gallery-nav--next");
  if (!gallery || !previousButton || !nextButton) return;

  const updateNavigation = () => {
    const maximumScroll = gallery.scrollWidth - gallery.clientWidth;
    previousButton.disabled = gallery.scrollLeft <= 2;
    nextButton.disabled = gallery.scrollLeft >= maximumScroll - 2;
  };

  const scrollGallery = (direction) => {
    gallery.scrollBy({ left: direction * gallery.clientWidth * 0.8, behavior: "smooth" });
  };

  previousButton.addEventListener("click", () => scrollGallery(-1));
  nextButton.addEventListener("click", () => scrollGallery(1));
  gallery.addEventListener("scroll", updateNavigation, { passive: true });
  window.addEventListener("resize", updateNavigation);
  updateNavigation();
});

const lightbox = document.getElementById("image-lightbox");
const lightboxImage = lightbox?.querySelector(".lightbox-image");
const lightboxVideo = lightbox?.querySelector(".lightbox-video");
const lightboxTitle = document.getElementById("lightbox-title");
const lightboxBrand = document.getElementById("lightbox-brand");
const lightboxClose = lightbox?.querySelector(".lightbox-close");
const lightboxPrevious = lightbox?.querySelector(".lightbox-nav--prev");
const lightboxNext = lightbox?.querySelector(".lightbox-nav--next");
const galleryShots = document.querySelectorAll("[data-lightbox], [data-video-lightbox]");
let activeGalleryShots = [];
let activeShotIndex = -1;

function closeLightbox() {
  if (!lightbox?.open) return;
  lightbox.close();
}

function showLightboxShot(shot) {
  const sourceImage = shot?.querySelector("img");
  const sourceVideo = shot?.querySelector("video");
  if (!lightbox || !lightboxImage || (!sourceImage && !sourceVideo)) return;

  if (sourceVideo && lightboxVideo) {
    lightboxImage.hidden = true;
    lightboxImage.src = "";
    lightboxVideo.pause();
    lightboxVideo.hidden = false;
    lightboxVideo.src = sourceVideo.currentSrc || sourceVideo.src;
    lightboxVideo.poster = sourceVideo.poster;
    lightboxVideo.load();
    lightboxVideo.play().catch(() => {});
  } else if (sourceImage) {
    if (lightboxVideo) {
      lightboxVideo.pause();
      lightboxVideo.hidden = true;
      lightboxVideo.removeAttribute("src");
      lightboxVideo.load();
    }
    lightboxImage.hidden = false;
    lightboxImage.src = sourceImage.currentSrc || sourceImage.src;
    lightboxImage.alt = sourceImage.alt;
  }

  const activeDictionary = translations[document.documentElement.lang] || translations.ru;
  const titleKey = shot.dataset.lightboxTitleKey;
  const brandKey = shot.dataset.lightboxBrandKey;
  if (lightboxTitle) lightboxTitle.textContent = (titleKey && activeDictionary[titleKey]) || shot.dataset.lightboxTitle || "1XGames";
  if (lightboxBrand) lightboxBrand.textContent = (brandKey && activeDictionary[brandKey]) || shot.dataset.lightboxBrand || "1XGames";
}

function updateLightboxNavigation() {
  const hasMultipleShots = activeGalleryShots.length > 1;
  if (lightboxPrevious) lightboxPrevious.hidden = !hasMultipleShots;
  if (lightboxNext) lightboxNext.hidden = !hasMultipleShots;
}

function moveLightbox(direction) {
  if (activeGalleryShots.length < 2) return;
  activeShotIndex = (activeShotIndex + direction + activeGalleryShots.length) % activeGalleryShots.length;
  showLightboxShot(activeGalleryShots[activeShotIndex]);
}

galleryShots.forEach((shot) => {
  shot.addEventListener("click", () => {
    const gallery = shot.closest(".project-gallery");
    activeGalleryShots = gallery
      ? [...gallery.querySelectorAll("[data-lightbox], [data-video-lightbox]")]
      : [shot];
    activeShotIndex = activeGalleryShots.indexOf(shot);
    showLightboxShot(shot);
    updateLightboxNavigation();
    document.body.classList.add("lightbox-open");
    lightbox.showModal();
  });
});

lightboxClose?.addEventListener("click", closeLightbox);
lightboxPrevious?.addEventListener("click", () => moveLightbox(-1));
lightboxNext?.addEventListener("click", () => moveLightbox(1));
document.addEventListener("keydown", (event) => {
  if (!lightbox?.open) return;
  if (event.key === "ArrowLeft") {
    event.preventDefault();
    moveLightbox(-1);
  } else if (event.key === "ArrowRight") {
    event.preventDefault();
    moveLightbox(1);
  }
});
lightbox?.addEventListener("click", (event) => {
  if (event.target === lightbox) closeLightbox();
});
lightbox?.addEventListener("close", () => {
  document.body.classList.remove("lightbox-open");
  activeGalleryShots = [];
  activeShotIndex = -1;
  if (lightboxImage) lightboxImage.src = "";
  if (lightboxVideo) {
    lightboxVideo.pause();
    lightboxVideo.hidden = true;
    lightboxVideo.removeAttribute("src");
    lightboxVideo.load();
  }
});

const galleryVideos = document.querySelectorAll(".gallery-video");
const prefersReducedMotion = window.matchMedia("(prefers-reduced-motion: reduce)").matches;

if (prefersReducedMotion) {
  galleryVideos.forEach((video) => video.pause());
} else if ("IntersectionObserver" in window) {
  const videoObserver = new IntersectionObserver(
    (entries) => {
      entries.forEach((entry) => {
        const video = entry.target;
        if (entry.isIntersecting) {
          video.play().catch(() => {});
        } else {
          video.pause();
        }
      });
    },
    { threshold: 0.25 },
  );

  galleryVideos.forEach((video) => videoObserver.observe(video));
}

const revealElements = document.querySelectorAll(".reveal");

if ("IntersectionObserver" in window && !window.matchMedia("(prefers-reduced-motion: reduce)").matches) {
  const revealObserver = new IntersectionObserver(
    (entries, observer) => {
      entries.forEach((entry) => {
        if (!entry.isIntersecting) return;
        entry.target.classList.add("is-visible");
        observer.unobserve(entry.target);
      });
    },
    { threshold: 0.12 },
  );

  revealElements.forEach((element) => revealObserver.observe(element));
} else {
  revealElements.forEach((element) => element.classList.add("is-visible"));
}
